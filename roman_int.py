def int_rom_2(intg):
    keys=[1000, 900, 500, 400, 100, 90, 50, 40, 10, 9, 5, 4, 1]
    value=['M', 'CM', 'D', 'CD', 'C', 'XC', 'L', 'XL', 'X', 'IX', 'V', 'IV', 'I']
    ans=""
    i=0
    while intg>0:
        if keys[i]<=intg:
            intg=intg-keys[i]
            ans=ans+value[i]
        else:
            i=i+1
    return(ans)

